# Step 1: Store the dataset
temperatures = [28, 30, 32, 31, 29, 27, 26]

# Step 2: Calculate average temperature
average_temp = sum(temperatures) / len(temperatures)

# Step 3: Find the highest and lowest temperature
highest_temp = max(temperatures)
lowest_temp = min(temperatures)

# Step 4: Print the results
print("Average Temperature:", average_temp)
print("Highest Temperature:", highest_temp)
print("Lowest Temperature:", lowest_temp)

#Explain the Results
#Average Temperature (29.0°C) – The week’s average temperature was 29°C.
#Highest Temperature (32°C) – The warmest day was Wednesday.
#Lowest Temperature (26°C) – The coolest day was Sunday.